public class LastXGames {

	private double fp;
	private double min;
	public LastXGames(double fp, double min)
	{
		this.fp = fp;
		this.min = min;
	}
	public double getFp() {
		return fp;
	}
	public void setFp(double fp) {
		this.fp = fp;
	}
	public double getMin() {
		return min;
	}
	public void setMin(double min) {
		this.min = min;
	}
}
